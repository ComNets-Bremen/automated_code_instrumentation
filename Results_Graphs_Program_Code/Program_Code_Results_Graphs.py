import matplotlib.pyplot as plt
import numpy as np
import seaborn as sns

# Global style configuration
sns.set_theme(style="white") # Changed style to remove grid
plt.rcParams.update({
    "font.family": "STIXGeneral",          # Times-like serif font (bundled with Matplotlib)
    "axes.edgecolor": "black",
    "axes.linewidth": 1.2,
    "figure.dpi": 400,
    "axes.titlesize": 14,
    "axes.labelsize": 13,
    "legend.fontsize": 12,
    "xtick.labelsize": 11,
    "ytick.labelsize": 11
})

# Academic color-blind-safe palette
COLOR_SETS = {
    "Python_Arduino": ("#0077B6", "#D62828"),  # rich deep blue / strong red
    "Python_Repos": ("#90E0EF", "#0077B6"),   # soft aqua blue / rich deep blue
    "Arduino_Repos": ("#FFB5A7", "#D62828")    # pastel coral / strong red
}

# Data
models = [
    "Rule Based Logging Model (Python)",
    "LLM Rule Based Model",
    "Prompt Based Fine Tuning of LLM",
    "Supervised Code to Code Fine Tuning of LLM"
]

# Figure 1 (Python vs Arduino)
python_match = [99.9, 94.3, 61.42, 47.20]
arduino_match = [99.9, 91.09, 78.37, 70.98]

# Figure 2 (Python Repositories)
python_repo1 = [99.9, 94.70, 60.93, 48.38]
python_repo2 = [99.9, 93.90, 61.92, 46.03]

# Figure 3 (Arduino Repositories)
arduino_repo1 = [99.9, 86.58, 66.45, 72.32]
arduino_repo2 = [99.9, 95.61, 90.29, 69.65]

# Generic plotting function
def plot_match_percentages(title, categories, data1, data2, label1, label2, filename, colors):
    x = np.arange(len(categories))
    width = 0.175 # Halved the width

    fig, ax = plt.subplots(figsize=(8.5, 5))
    bars1 = ax.bar(x - width/2, data1, width, color=colors[0],
                   label=label1, edgecolor='black', linewidth=0.8)
    bars2 = ax.bar(x + width/2, data2, width, color=colors[1],
                   label=label2, edgecolor='black', linewidth=0.8)

    # Labels and title
    ax.set_title(title, fontweight='bold', pad=16)
    ax.set_ylabel('Match Percentage (%)', fontweight='bold')
    ax.set_xlabel('Model Type', fontweight='bold')
    ax.set_xticks(x)
    # Modified xtick labels to be in two rows
    ax.set_xticklabels([label.replace("Based", "Based\n").replace("Fine Tuning", "Fine Tuning\n") for label in categories], rotation=0, ha='center', fontweight='bold') # Made x-tick labels bold
    ax.set_ylim(0, 110)
    ax.legend(frameon=False, loc='upper right')

    # Value labels
    for bars in [bars1, bars2]:
        for bar in bars:
            h = bar.get_height()
            ax.text(bar.get_x() + bar.get_width()/2, h + 1,
                    f'{h:.2f}', ha='center', va='bottom', fontsize=8, fontweight='bold') # Removed '%'

    sns.despine()
    plt.tight_layout()
    plt.savefig(filename, bbox_inches='tight', dpi=400)
    plt.close()

# Produce the three figures
plot_match_percentages(
    "Match Percentage of Rule-Based and Fine-Tuned Models",
    models, python_match, arduino_match,
    "Python Code", "Arduino Code",
    "Match_Percentage_Python_vs_Arduino_Final.png",
    COLOR_SETS["Python_Arduino"]
)

plot_match_percentages(
    "Match Percentage Analysis Using Two Python Code Repositories",
    models, python_repo1, python_repo2,
    "Python Repository 1", "Python Repository 2",
    "Match_Percentage_Python_Repositories_Final.png",
    COLOR_SETS["Python_Repos"]
)

plot_match_percentages(
    "Match Percentage Analysis Using Two Arduino Code Repositories",
    models, arduino_repo1, arduino_repo2,
    "Arduino Repository 1", "Arduino Repository 2",
    "Match_Percentage_Arduino_Repositories_Final.png",
    COLOR_SETS["Arduino_Repos"]
)

print(" All three color graphs generated successfully.")

